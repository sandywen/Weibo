package org.apache.hadoop.test;

public class User {
	private int weibo_number;
	private String type;
	private int total_retweet;
	private int total_review;
	private int total_zan;
	public int getWeibo_number() {
		return weibo_number;
	}
	public void setWeibo_number(int weibo_number) {
		this.weibo_number = weibo_number;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getTotal_retweet() {
		return total_retweet;
	}
	public void setTotal_retweet(int total_retweet) {
		this.total_retweet = total_retweet;
	}
	public int getTotal_review() {
		return total_review;
	}
	public void setTotal_review(int total_review) {
		this.total_review = total_review;
	}
	public int getTotal_zan() {
		return total_zan;
	}
	public void setTotal_zan(int total_zan) {
		this.total_zan = total_zan;
	}
	
	
}
